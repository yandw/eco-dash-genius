export type AdvancedFilters = Record<string, string[]>;

export const emptyAdvancedFilters = (): AdvancedFilters =>
  ARCHIVE_FILTER_GROUPS.reduce<AdvancedFilters>((acc, g) => {
    acc[g.key] = [];
    return acc;
  }, {});

export const countAdvancedFilters = (f: AdvancedFilters) =>
  Object.values(f).reduce((sum, arr) => sum + arr.length, 0);

export const findGroupLabel = (key: string) =>
  ARCHIVE_FILTER_GROUPS.find((g) => g.key === key)?.label ?? key;
